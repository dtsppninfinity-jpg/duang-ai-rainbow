// ===== Types =====

export type DayColor = {
  dayKey: number; // 0=อาทิตย์ .. 6=เสาร์ (ตรงกับ Date.getDay())
  // ชื่อวันแบบเต็มรวมคำว่า "วัน" แล้ว (เช่น "วันอาทิตย์") — เรนเดอร์ verbatim
  dayTH: string;
  mainColor: string;
  mainColorHex: string;
  luckyColors: string[];
  badColor: string;
  // บุคลิกตามวันเกิด — เอนจินนำไปผสานลงฟิลด์ข้อความเดิมของ Reading (เช่น overall)
  personality: string;
};

export type LifePath = {
  title: string;
  traits: string;
  love: string;
  money: string;
  work: string;
  health: string;
};

// คีย์เป็น "1".."9"
export type LifePaths = Record<string, LifePath>;

export type ZodiacTH = {
  index: number; // 0=ชวด .. 11=กุน
  nameTH: string;
  // คำอ่านไทยตามตำราดั้งเดิม (มะโรง="งูใหญ่", มะเส็ง="งูเล็ก") — ห้าม hardcode ที่อื่น
  animal: string;
  traits: string;
};

export type ZodiacWestern = {
  nameTH: string;
  nameEN: string;
  start: { m: number; d: number };
  end: { m: number; d: number };
  traits: string;
};

export type PredictionCategory =
  | "love"
  | "money"
  | "work"
  | "health"
  | "overall";

export type Predictions = Record<PredictionCategory, string[]>;

export type AstroData = {
  dayColors: DayColor[];
  lifePaths: LifePaths;
  zodiacTH: ZodiacTH[];
  zodiacWestern: ZodiacWestern[];
  predictions: Predictions;
};

